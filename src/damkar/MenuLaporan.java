    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
        int baris_terpilih = jTable1.getSelectedRow();

        if (baris_terpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih laporan yang akan diubah statusnya!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String status_lama = jTable1.getValueAt(baris_terpilih, 6).toString();

        // Pastikan status lama bukan "baru" sebelum melanjutkan
        if (status_lama.equalsIgnoreCase("baru")) {
            JOptionPane.showMessageDialog(this, "Kirimkan Dulu regunya", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String status_terpilih = "Selesai";
        String id_laporan = jTable1.getValueAt(baris_terpilih, 0).toString();
        String id_unit = null;

        // Menggunakan try-with-resources untuk koneksi dan PreparedStatement
        try (Connection conn = damkar.Koneksi.db_koneksi()) {
            String sql_cariID_unit = "SELECT id_unit FROM penugasan WHERE id_laporan = ? LIMIT 1";

            try (PreparedStatement pst_cariID_unit = conn.prepareStatement(sql_cariID_unit)) {
                pst_cariID_unit.setString(1, id_laporan);
                try (ResultSet rs_cariID_unit = pst_cariID_unit.executeQuery()) {
                    if (rs_cariID_unit.next()) {
                        id_unit = rs_cariID_unit.getString("id_unit");
                    }
                }
            }

            if (id_unit != null) {
                // Update status unit jika id_unit ditemukan
                String sql_update_unit = "UPDATE unit_damkar SET status = 'Tersedia' WHERE id_unit = ?";
                try (PreparedStatement pst_updateUnit = conn.prepareStatement(sql_update_unit)) {
                    pst_updateUnit.setString(1, id_unit);
                    pst_updateUnit.executeUpdate();
                }
            }

            // Update status laporan menjadi "Selesai"
            String sql_updateLaporan = "UPDATE laporan SET status_laporan = ? WHERE id_laporan = ?";
            try (PreparedStatement pst_updateLaporan = conn.prepareStatement(sql_updateLaporan)) {
                pst_updateLaporan.setString(1, status_terpilih);
                pst_updateLaporan.setString(2, id_laporan);
                pst_updateLaporan.executeUpdate();
            }

            JOptionPane.showMessageDialog(this, "Status laporan berhasil diperbarui!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memperbarui status laporan");
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnSelesaiActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow >= 0) {
            tfPelapor.setText(model.getValueAt(selectedRow, 1).toString());
            TAalamat.setText(model.getValueAt(selectedRow, 2).toString());
            tfWaktu.setText(model.getValueAt(selectedRow, 3).toString());
            tfJenisLaporan.setText(model.getValueAt(selectedRow, 4).toString());
            tfKontak.setText(model.getValueAt(selectedRow, 5).toString());

            String status = model.getValueAt(selectedRow, 6).toString();

            switch (status) {

                case "baru":
                    rbBaru.setSelected(true);
                    break;

                case "diproses":
                    rbDiproses.setSelected(true);
                    break;

                case "selesai":
                    rbSelesai.setSelected(true);
                    break;
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

