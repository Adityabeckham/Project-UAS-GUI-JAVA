    public void tampil_data() {
        // Gunakan try-with-resources untuk memastikan koneksi dan PreparedStatement ditutup setelah selesai digunakan
        try (Connection conn = damkar.Koneksi.db_koneksi(); PreparedStatement pst = conn.prepareStatement(
                "SELECT l.id_laporan, l.pelapor, l.alamat,l.waktu_laporan,l.jenis_laporan,"
                + "l.kontak_pelapor,l.status_laporan,u.nama_unit "
                + "FROM laporan l "
                + "LEFT JOIN penugasan pg ON l.id_laporan = pg.id_laporan "
                + "LEFT JOIN unit_damkar u ON pg.id_unit = u.id_unit "
                + "WHERE l.status_laporan != 'Selesai' "
                + "GROUP BY l.id_laporan "
                + "ORDER BY l.waktu_laporan DESC")) {

            // Eksekusi query
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                String id_laporan = rs.getString("id_laporan");
                String pelapor = rs.getString("pelapor");
                String alamat = rs.getString("alamat");
                String waktu = rs.getString("waktu_laporan");
                String jenis = rs.getString("jenis_laporan");
                String kontak = rs.getString("kontak_pelapor");
                String status = rs.getString("status_laporan");
                String unit = rs.getString("nama_unit");

                if (unit == null) {
                    unit = "-";
                }

                model.addRow(new Object[]{
                    id_laporan, pelapor, alamat, waktu, jenis, kontak, status, unit
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memuat data", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

