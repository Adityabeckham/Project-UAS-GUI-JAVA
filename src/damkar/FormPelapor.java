    private void btnKirimLaporanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKirimLaporanActionPerformed
        // TODO add your handling code here:
        String pelapor = tfPelaporr.getText();
        String alamat = TAalamatpelapor.getText();
        String jenis = tfJenis.getText();
        String kontak = tfKontak.getText();

        LocalDateTime sekarang = LocalDateTime.now();
        DateTimeFormatter format = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String waktu = sekarang.format(format);
        String status = "baru";

        if (pelapor.isEmpty() || alamat.isEmpty() || jenis.isEmpty() || kontak.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            java.sql.Connection conn = damkar.Koneksi.db_koneksi();
            String sql = "INSERT INTO laporan "
                    + "(id_laporan, pelapor, alamat, waktu_laporan, kontak_pelapor, jenis_laporan, status_laporan) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            String id_laporan = "LRP-" + UUID.randomUUID().toString(); // Menggunakan UUID penuh

            pst.setString(1, id_laporan);
            pst.setString(2, pelapor);
            pst.setString(3, alamat);
            pst.setString(4, waktu);
            pst.setString(5, kontak);
            pst.setString(6, jenis);
            pst.setString(7, status);

            pst.executeUpdate();
            pst.close();

            // Reset fields
            tfPelaporr.setText("");
            TAalamatpelapor.setText("");
            tfJenis.setText("");
            tfKontak.setText("");

            tfPelaporr.requestFocus();

            JOptionPane.showMessageDialog(this, "Laporan berhasil dikirim!");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengirim laporan:\n" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnKirimLaporanActionPerformed

