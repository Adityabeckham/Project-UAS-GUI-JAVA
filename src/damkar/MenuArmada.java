    private void muat_semua_unit() {

        String sql = "SELECT jenis_unit FROM unit_damkar ORDER BY jenis_unit DESC";

        try {
            Connection conn = damkar.Koneksi.db_koneksi();
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            cbUnitArmada.removeAllItems();

            while (rs.next()) {
                String jenis_unit = rs.getString("jenis_unit");
                cbUnitArmada.addItem(jenis_unit);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data unit: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void filter_unit(String jenis) {

        String sql = "SELECT status from unit_damkar Where jenis_unit = ?";

        try {
            Connection conn = damkar.Koneksi.db_koneksi();
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, jenis);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                String status = rs.getString("status");

                lb_status.setText(status.toUpperCase());

                switch (status.toLowerCase()) {

                    case "tersedia":
                        pnl_status.setBackground(new java.awt.Color(0, 255, 0)); // Hijau
                        break;
                    case "bertugas":
                        pnl_status.setBackground(new java.awt.Color(255, 0, 0)); // Merah
                        break;
                    default:
                        pnl_status.setBackground(java.awt.Color.GRAY);
                        break;
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

